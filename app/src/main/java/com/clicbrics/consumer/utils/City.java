package com.clicbrics.consumer.utils;


import java.util.List;

/**
 * Created by Mehar on 06-10-2015.
 */
public class City {
    private Long id;
    private String name;
    private String state;
    private String country;
    private String zone;
    private float lat;
    private float lng;
    private String timeZone;
    private List<Integer> allowedCities;
    private String pName;

    public City() {
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

    public String getCountry() {
        return country;
    }

    public void setCountry(String country) {
        this.country = country;
    }

    public String getZone() {
        return zone;
    }

    public void setZone(String zone) {
        this.zone = zone;
    }

    public void setCenterGeopt(float lat, float lng) {
        this.lat = lat;
        this.lng = lng;
    }

    public float getLat() {
        return lat;
    }

    public void setLat(float lat) {
        this.lat = lat;
    }

    public float getLng() {
        return lng;
    }

    public void setLng(float lng) {
        this.lng = lng;
    }

    public String getTimeZone() {
        return timeZone;
    }

    public void setTimeZone(String timeZone) {
        this.timeZone = timeZone;
    }

    public List<Integer> getAllowedCities() {
        return allowedCities;
    }

    public void setAllowedCities(List<Integer> allowedCities) {
        this.allowedCities = allowedCities;
    }

    public String getpName() {
        return pName;
    }

    public void setpName(String pName) {
        this.pName = pName;
    }
}
