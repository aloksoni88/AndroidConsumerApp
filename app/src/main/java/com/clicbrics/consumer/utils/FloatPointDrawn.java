package com.clicbrics.consumer.utils;

/**
 * Created by Paras on 05-09-2016.
 * Class responsiable for handling floating points of drawn shape
 */
public class FloatPointDrawn {
    float x, y;

    public FloatPointDrawn(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }
}
